package de.lacodev.rsystem.objects;

public class BanReasons {


    public String name;

    public Integer id;



    public BanReasons(String name, Integer id) {

        this.name = name;

        this.id = id;

    }



    public String getName() {

        return this.name;

    }



    public Integer getID() {

        return this.id;

    }
	
}
